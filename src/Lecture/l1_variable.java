package Lecture;

public class l1_variable {
    static void main(String[] args) {
        int num;
        num = 10;
        System.out.println(num);
        num = 45;
        System.out.println(num);
        System.out.println(num = 90);

        float myflot = 9.98f;
        System.out.println(myflot);

        double mydouble = 5.2343;
        System.out.println(mydouble);

        String name = "Rishi";
        System.out.println(name);

        String a = "Rishiiii";
        System.out.println(a);
    }
}





